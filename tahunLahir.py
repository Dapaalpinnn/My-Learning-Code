# Menghitung umur di tahun 2024 sekarang
tahunLahir = int(input("Masukkan tahun lahirmu: "))
usiaSekarang = 2024 - tahunLahir
print(f"Usia kamu sekarang adalah {usiaSekarang} tahun")

# Mengetahui tahun saat umur ke n
tahunN = int(input("Masukkan usia yang kamu inginkan untuk melihat tahun berapa kamu diusia tersebut: "))

# Hitung Umur
tahunKeDepan = tahunLahir + tahunN
print(f"Kamu akan berusia {tahunN} di tahun {tahunKeDepan}")